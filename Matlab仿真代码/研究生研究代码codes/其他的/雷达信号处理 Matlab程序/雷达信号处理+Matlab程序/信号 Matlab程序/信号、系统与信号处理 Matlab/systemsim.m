%产生系统仿真的程序示例
%模拟系统
a = [1,2,1];         %建立微分方程y(t)的系数
b = [1];             %建立微分方程x(t)的系数
sys = tf(b,a);       %建立系统方程
t = 0:0.01:4;        %定义响应时间的区间
h=impulse(sys,t);    %求模拟系统的冲激响应
figure;plot(t,h)     %绘出冲激响应的结果
 

%离散系统
a = [1,-1/6,-1/6];   %建立差分方程y(n)的系数
b = [1];             %建立差分方程x(n)的系数
n = 0:100;           %定义响应时间的区间
h=impz(b,a,n);       %求离散系统的冲激响应
figure;stem(n,h)     %绘出冲激响应的结果
